// Helper functions for getting unique values
export const getUniqueMediaTypes = () => {
  const mediaSet = new Set();
  artists.forEach(artist => {
    artist.medium.forEach(medium => {
      mediaSet.add(medium);
    });
  });
  return [...mediaSet];
};

export const getUniqueLocations = () => {
  return [...new Set(artists.map(artist => artist.location))];
};

export const getUniqueInvisibleThreads = () => {
  const threadSet = new Set();
  artists.forEach(artist => {
    artist.invisibleThread.forEach(thread => {
      threadSet.add(thread);
    });
  });
  return [...threadSet];
};